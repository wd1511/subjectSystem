package subjectSystem;

public class student {
	private String studentID;
	private String studentName;
	private String birthday;
	private String sex;
	private String phoneNum;
	private String fieldID;
	private String password;
	
	public String getStudentID(){
		return studentID;
	}
	
	public void setStudentID(String studentID1){
		studentID=studentID1;
	}
	
	public String getStudentName(){
		return studentName;
	}
	
	public void setStudentName(String studentName1){
		studentName=studentName1;
	}
	
	public String getBirthday(){
		return birthday;
	}
	
	public void setBirthday(String birthday1){
		birthday=birthday1;
	}
	
	public String getSex(){
		return sex;
	}
	
	public void setSex(String sex1){
		sex=sex1;
	}
	
	public String getPhoneNum(){
		return phoneNum;
	}
	
	public void setPhoneNum(String phoneNum1){
		phoneNum=phoneNum1;
	}
	
	public String getFieldID(){
		return fieldID;
	}
	
	public void setFieldID(String fieldID1){
		fieldID=fieldID1;
	}
	
	public String getPassword(){
		return password;
	}
	
	public void setPassword(String password1){
		password=password1;
	}
}
