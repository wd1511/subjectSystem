package subjectSystem;

public class teacher {
	private String teacherID;
	private String teacherName;
	private String birthday;
	private String sex;
	private String phoneNum;
	private String schoolID;
	private String password;
	
	public String getTeacherID(){
		return teacherID;
	}
	
	public void setTeacherID(String teacherID1){
		teacherID=teacherID1;
	}
	
	public String getTeacherName(){
		return teacherName;
	}
	
	public void setTeacherName(String teacherName1){
		teacherName=teacherName1;
	}
	
	public String getBirthday(){
		return birthday;
	}
	
	public void setBirthday(String birthday1){
		birthday=birthday1;
	}
	
	public String getSex(){
		return sex;
	}
	
	public void setSex(String sex1){
		sex=sex1;
	}
	
	public String getPhoneNum(){
		return phoneNum;
	}
	
	public void setPhoneNum(String phoneNum1){
		phoneNum=phoneNum1;
	}
	
	public String getSchoolID(){
		return schoolID;
	}
	
	public void setSchoolID(String schoolID1){
		schoolID=schoolID1;
	}
	
	public String getPassword(){
		return password;
	}
	
	public void setPassword(String password1){
		password=password1;
	}
}
